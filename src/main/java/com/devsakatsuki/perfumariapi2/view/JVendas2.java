    private void limpaVenda() {
        venda = null;
        ftxtValorTotal.setValue(null);
        tblItensVenda.setModel(new ItemVendaTableModel());
        spnQuantidade.setValue(1);
        ftxtCliente.setValue(null);
        ftxtProduto.setValue(null);
        ftxtPreco.setValue(null);
    }

