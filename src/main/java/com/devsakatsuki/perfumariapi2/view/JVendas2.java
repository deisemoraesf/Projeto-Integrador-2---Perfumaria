    private void btnRemoverItensActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverItensActionPerformed
        if (tblItensVenda.getSelectedRowCount() > 0) {
            int linha = tblItensVenda.getSelectedRow();
            ItemVendaTableModel ivtm = (ItemVendaTableModel) tblItensVenda.getModel();
            ItemVenda iv = ivtm.getRowValue(linha);

            if (JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o item " + iv + "?", "Confirma√ß√£o", JOptionPane.YES_NO_OPTION) == 0) {
                venda.removeItem(iv);
                ivtm.setDados(venda.getItens());
                ftxtValorTotal.setValue(nfc.format(venda.calculaValorTotal()));
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma linha para remover.", "Alerta", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnRemoverItensActionPerformed

    private void btnAdicionarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarProdutoActionPerformed
        ItemVenda iv = new ItemVenda();
        
        if (ftxtProduto.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Selecione um produto", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Produto prod = (Produto) ftxtProduto.getValue();        
        if (prod.getQuantidade() > 0) {
            iv.setProduto((Produto) ftxtProduto.getValue());
            iv.setVenda(venda);
            if (prod.getQuantidade() >= (int) spnQuantidade.getValue()) {
                iv.setQuantidade((int) spnQuantidade.getValue());
            } else {
                JOptionPane.showMessageDialog(null, "N√£o h√° quantidades suficientes. " + prod.getQuantidade() + " Quantidades dispon√≠veis.", "Aviso", JOptionPane.WARNING_MESSAGE);
            }
            iv.setValorUnitario(prod.getPreco());
            if ((int) spnQuantidade.getValue() > 0 && (int) spnQuantidade.getValue() <= prod.getQuantidade()) {
                venda.addItem(iv);
            } else {
                JOptionPane.showMessageDialog(null, "Escolha a quantidade.", "Aviso", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "N√£o h√° produto no estoque.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }

        ItemVendaTableModel ivtm = (ItemVendaTableModel) tblItensVenda.getModel();
        ivtm.setDados(venda.getItens());

        ftxtValorTotal.setValue(nfc.format(venda.calculaValorTotal()));
    }//GEN-LAST:event_btnAdicionarProdutoActionPerformed

